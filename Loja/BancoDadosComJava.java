    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        try {
            String wpesquisar = "select * from produto " + "where codigo = " + jTextField4.getText();
            // public ResultSet resultado = null;
            resultado = st.executeQuery(wpesquisar);
            if (resultado.next()) {
                jLabel4.setText(resultado.getString("codigo"));
                jTextField1.setText(resultado.getString("nome"));
                jTextField2.setText(resultado.getString("quantidade_vendida"));
                jTextField3.setText(resultado.getString("preco_produto"));
                jTextField5.setText(resultado.getString("datas"));
                JOptionPane.showMessageDialog(null, "Registo Encontrado");
            } else {

                JOptionPane.showMessageDialog(null, "Registo n√£o Encontrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }                                        

